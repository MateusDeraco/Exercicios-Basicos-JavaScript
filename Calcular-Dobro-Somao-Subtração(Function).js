//Funções dobro, somar e subtrair que serão exportadas para "exercicios-1.js"
export function dobro(n) {
    let resultado = n*2
    alert(`O dobro de ${n} é ${resultado}`)}

export function somar(x, y) {
    let resultado = x + y
    alert(`${x} + ${y} = ${resultado}`)}

export function subtrair(x, y) {
    let resultado = x - y
    alert(`${x} - ${y} = ${resultado}`)}