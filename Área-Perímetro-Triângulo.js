//Calcula a área de um triângulo
let altura = parseFloat(prompt("Insira a altura do triângulo"))
let base =  parseFloat(prompt("Insira a base do triângulo"))

//Exibe o resultado do calculo da área
alert(`A área do triânngulo é ${(altura*base)/2}`)


//Calcula o perímetro do triângulo
let ladoA = parseFloat(prompt("Insira a medida do lado A do triângulo"))
let ladoB = parseFloat(prompt("Insira a medida do lado B do triângulo"))
let ladoC = parseFloat(prompt("Insira a medida do lado C do triângulo"))

//Exibe o Resultado do calculo do perímetro
alert(`O perímetro do triângulo é ${ladoA + ladoB + ladoC}`)